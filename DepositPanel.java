    private void ConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmButtonActionPerformed
       String input = AmountField.getText().trim();

if (input.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please enter an amount.");
    return;
}

double depositAmount;
try {
    depositAmount = Double.parseDouble(input);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Invalid number format.");
    return;
}

if (depositAmount == 0) {
    JOptionPane.showMessageDialog(this, "Cannot deposit 0.");
} else if (depositAmount > 10000) {
    JOptionPane.showMessageDialog(this, "Cannot deposit more than 10,000.");
} else {
    JOptionPane.showMessageDialog(this, "Deposit successful, your account balance has been updated!");
}

    }//GEN-LAST:event_ConfirmButtonActionPerformed

